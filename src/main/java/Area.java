public class Area {
    private int weith;
    private int hight;

    public Area(int weith, int hight) {
        this.weith = weith;
        this.hight = hight;
    }

    public int getWeith() {
        return weith;
    }

    public void setWeith(int weith) {
        this.weith = weith;
    }

    public int getHight() {
        return hight;
    }

    public void setHight(int hight) {
        this.hight = hight;
    }
}
